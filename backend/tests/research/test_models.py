def test_source_deduplication_by_hash() -> None:
    source1 = ResearchSource(
        url="https://example.com/article",
        canonical_url="https://example.com/article",
        domain="example.com",
        title="Same Title",
        snippet="Same Snippet",
        content_hash="abc123",
    )
    
    source2 = ResearchSource(
        url="https://example.com/article-duplicate",
        canonical_url="https://example.com/article",
        domain="example.com",
        title="Same Title",
        snippet="Same Snippet",
        content_hash="abc123",  # Same hash
    )
    
    # Should be detected as duplicate
    assert source1.content_hash == source2.content_hash
    assert source1.canonical_url == source2.canonical_url


def test_source_quality_filtering() -> None:
    sources = [
        ResearchSource(
            url=f"https://example.com/{i}",
            canonical_url=f"https://example.com/{i}",
            domain="example.com",
            title=f"Source {i}",
            snippet="Snippet",
            quality=quality,
        )
        for i, quality in enumerate([
            SourceQuality.HIGH,
            SourceQuality.MEDIUM,
            SourceQuality.LOW,
            SourceQuality.SPAM,
        ])
    ]
    
    high_quality = [s for s in sources if s.quality != SourceQuality.SPAM]
    assert len(high_quality) == 3
    assert all(s.quality != SourceQuality.SPAM for s in high_quality)


