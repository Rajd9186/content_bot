def test_research_source_creation() -> None:
    source = ResearchSource(
        url="https://example.com/article",
        canonical_url="https://example.com/article",
        domain="example.com",
        title="Test Article",
        snippet="Test snippet",
    )
    
    assert source.url == "https://example.com/article"
    assert source.domain == "example.com"
    assert source.quality == SourceQuality.MEDIUM
    assert source.content_hash is None  # Not auto-generated by model


def test_research_source_quality_scoring() -> None:
    high_quality = ResearchSource(
        url="https://arxiv.org/paper",
        canonical_url="https://arxiv.org/paper",
        domain="arxiv.org",
        title="Academic Paper",
        snippet="Peer-reviewed research",
        authors=["Dr. Author"],
        quality=SourceQuality.HIGH,
    )
    
    assert high_quality.quality == SourceQuality.HIGH
    # Source type is not auto-classified from domain in the model
    # (classification happens in ResearchPipeline._normalize_source)
    assert high_quality.source_type == SourceType.WEB


